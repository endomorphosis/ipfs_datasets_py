def _deterministic_case_entry(case_id: str, result: HammerResult) -> dict:
    """A report entry for a fully synthetic (fixed-timestamp) case: every
    field, including ``receipt_id``, is byte-for-byte reproducible."""

    from ipfs_datasets_py.logic.hammers.receipts import HammerReceipt

    receipt = HammerReceipt(result=result)
    return {
        "case_id": case_id,
        "kind": "deterministic",
        "theorem_id": result.request.theorem_id,
        "status": result.status.value,
        "corpus_revision": result.corpus_revision,
        "receipt_id": receipt.receipt_id,
        "result_digest": compute_content_digest(result.to_dict()),
    }


def _real_kernel_case_entry(case_id: str, result: HammerResult, evidence) -> dict:
    """A report entry for a case that invoked a genuine ITP kernel
    subprocess: only content-derived (never wall-clock-derived) fields are
    recorded, since ``result``/receipt digests legitimately vary run to run
    with real subprocess timing."""

    reconstruction = result.reconstruction
    assert reconstruction is not None and reconstruction.kernel_accepted is True, (
        f"{case_id}: every claimed VERIFIED golden case must carry an actual "
        "kernel_accepted=True ReconstructionRecord"
    )
    command = list(getattr(evidence, "command", []) or [])
    return {
        "case_id": case_id,
        "kind": "real_kernel",
        "theorem_id": result.request.theorem_id,
        "status": result.status.value,
        "corpus_revision": result.corpus_revision,
        "itp": result.request.itp.value,
        "kernel_accepted": reconstruction.kernel_accepted,
        "kernel_command_executable_basename": (
            Path(command[0]).name if command else None
        ),
    }


def build_golden_report() -> dict:
    """Build the full HAMMER-014 golden-corpus report.

    Regenerated fresh on every ``test_end_to_end_hammer.py`` run. Every
    field under a ``"deterministic"``-kind case is content-derived (no real
    wall-clock timestamp anywhere in its object graph) and therefore
    byte-for-byte stable across runs/hosts; every field under a
    ``"real_kernel"``-kind case is likewise content-derived (deliberately
    excluding receipt/result digests, whose upstream records legitimately
    embed real subprocess wall-clock timing).
    """

    manifest = load_golden_corpus_manifest()
    corpus_revision = manifest.revision

    cases = [
        _deterministic_case_entry("candidate_only", build_candidate_only_case(manifest)[0]),
        _deterministic_case_entry("counterexample", build_counterexample_case(manifest)[0]),
        _deterministic_case_entry("timeout", build_timeout_case(manifest)),
        _deterministic_case_entry(
            "unsupported_translation", build_unsupported_translation_case(manifest)
        ),
    ]

    unavailable_result, _denied = build_unavailable_solver_case(manifest)
    cases.append(_deterministic_case_entry("unavailable_solver", unavailable_result))

    lean_result, lean_evidence = build_verified_lean_case(manifest)
    cases.append(_real_kernel_case_entry("verified_lean", lean_result, lean_evidence))

    coq_result, coq_evidence = build_verified_coq_case(manifest)
    cases.append(_real_kernel_case_entry("verified_coq", coq_result, coq_evidence))

    native_result, native_attempt = build_verified_via_native_automation_case(manifest)
    cases.append(
        _real_kernel_case_entry(
            "verified_native_automation_fallback", native_result, native_attempt.evidence
        )
    )

    return {
        "schema_version": "1.0.0",
        "corpus": {
            "manifest_id": manifest.manifest_id,
            "revision": corpus_revision,
            "theorem_count": len(manifest.entries),
        },
        "cases": cases,
    }

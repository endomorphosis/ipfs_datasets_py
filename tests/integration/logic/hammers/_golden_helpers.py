def build_verified_lean_case(manifest: CorpusManifest):
    """A genuine Lean goal, closed via a real ``lean`` kernel subprocess
    invoked by :class:`~ipfs_datasets_py.logic.hammers.reconstructors.lean.
    LeanReconstructor` (HAMMER-010) from an untrusted candidate produced by
    the real HAMMER-007/008/009 translation/provenance pipeline."""

    from ipfs_datasets_py.logic.hammers.frontends import LeanFrontend
    from ipfs_datasets_py.logic.hammers.reconstructors.lean import LeanReconstructor

    corpus_revision = manifest.revision
    source = LEAN_VERIFIED_GOOD_PATH.read_text(encoding="utf-8")
    theorem_id = "hammer_golden_verified"
    snapshot = LeanFrontend().snapshot_goal(source, theorem_id=theorem_id)

    request = HammerRequest(
        request_id="golden-verified-lean-1",
        itp=ITPKind.LEAN,
        theorem_id=theorem_id,
        goal_statement=snapshot.goal_text,
        corpus_revision=corpus_revision,
        policy=make_fixed_policy(allowed_solvers=["vampire"]),
    )
    premises = select_golden_premises(
        manifest, snapshot.goal_text, imports=snapshot.imports, top_k=2
    )

    ctx = TranslationContext(request_id=request.request_id)
    translation = ctx.translate(
        source_construct="goal",
        term=Eq(Const("n", NAT), Const("n", NAT)),
        target=TranslationTarget.TPTP,
    )

    stdout = (
        "% SZS status Theorem for hammer_golden_verified\n"
        f"fof(f1, axiom, n = n, file('src', '{premises[0].premise_id}')).\n"
        "fof(f2, plain, $false, inference(resolve, [], [f1])).\n"
    )
    attempt = SolverAttemptRecord(
        attempt_id=f"{request.request_id}:{translation.translation_id}:vampire:0",
        request_id=request.request_id,
        translation_id=translation.translation_id,
        solver_name="vampire",
        solver_version="fixture-1.0",
        target=TranslationTarget.TPTP,
        timeout_seconds=request.policy.timeout_seconds,
        verdict=SolverVerdict.PROVED,
        exit_code=0,
        wall_time_seconds=1.0,
        raw_output_digest=compute_content_digest({"stdout": stdout, "stderr": ""}),
    )
    attempt.validate()

    normalized = normalize_solver_evidence(
        request_id=request.request_id,
        attempt=attempt,
        raw_stdout=stdout,
        raw_stderr="",
        premise_ids=[p.premise_id for p in premises],
        translation_map=ctx.translation_map,
    )
    assert normalized.recommended_status is HammerResultStatus.CANDIDATE
    candidate = build_proof_candidate_record(
        normalized,
        candidate_id=compute_content_digest({"case": "verified-lean", "attempt": attempt.attempt_id}),
        request_id=request.request_id,
        solver_attempt_id=attempt.attempt_id,
    )

    record, evidence, lock = LeanReconstructor().reconstruct(
        request=request,
        candidate=candidate,
        goal_snapshot=snapshot,
        native_source=source,
    )
    assert record.kernel_accepted is True, record.failure_reason

    result = HammerResult(
        result_id=f"{request.request_id}:result",
        request=request,
        status=HammerResultStatus.VERIFIED,
        corpus_revision=corpus_revision,
        environment_lock=lock,
        premises=premises,
        translations=[translation],
        solver_attempts=[attempt],
        proof_candidate=candidate,
        reconstruction=record,
        notes=["kernel-checked via LeanReconstructor (HAMMER-010)"],
    )
    return result, evidence


def build_verified_coq_case(manifest: CorpusManifest):
    """The Coq/Rocq analogue of :func:`build_verified_lean_case`, exercising
    a different native ITP kernel through the same trust contract."""

    from ipfs_datasets_py.logic.hammers.frontends import CoqFrontend
    from ipfs_datasets_py.logic.hammers.reconstructors.coq import CoqReconstructor


def load_golden_corpus_manifest(
    *, manifest_id: str = "hammer-golden-fixture-manifest"
) -> CorpusManifest:
    """Build the golden :class:`CorpusManifest` from the declarative fixture
    JSON at :data:`GOLDEN_CORPUS_PATH` (same schema as
    ``tests/fixtures/logic/hammers/premise_selection_corpus.json``, consumed
    the same way ``benchmarks/bench_itp_hammer_premise_selection.py`` does).

    ``manifest_id`` is caller-assigned bookkeeping only -- it is not part of
    :attr:`CorpusManifest.revision`, which is a pure function of the
    registered source(s) and ingested theorem entries below.
    """

    payload = json.loads(GOLDEN_CORPUS_PATH.read_text(encoding="utf-8"))
    manifest = CorpusManifest(manifest_id=manifest_id)
    manifest.register_source(
        CorpusSource(
            corpus_id=payload["corpus_id"],
            name=payload["name"],
            source_itp=ITPKind(payload.get("source_itp", "lean")),
            version_ref=payload["version_ref"],
            license_id=payload["license_id"],
            license_url=payload.get("license_url"),
            description=payload.get("description", ""),
        )
    )
    for theorem in payload["theorems"]:
        manifest.add_theorem(
            theorem_id=theorem["theorem_id"],
            corpus_id=payload["corpus_id"],
            statement=theorem["statement"],
            imports=theorem.get("imports"),
        )
    return manifest



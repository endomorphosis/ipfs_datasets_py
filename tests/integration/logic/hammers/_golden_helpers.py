"""Shared, deterministic golden-corpus builders for the HAMMER-014
end-to-end and adversarial hammer test suites.

This module is intentionally *not* named ``test_*.py`` so pytest never
collects it directly -- both ``test_end_to_end_hammer.py`` and
``test_adversarial_hammer.py`` import it for a single shared source of
truth about:

- Loading the golden corpus manifest fixture
  (``tests/fixtures/logic/hammers/golden_corpus.json``) via the same
  declarative-JSON convention already used by
  ``benchmarks/bench_itp_hammer_premise_selection.py`` (HAMMER-005).
- A fixed, non-wall-clock timestamp used everywhere a record would
  otherwise stamp ``datetime.now()``, so every hand-assembled "golden"
  :class:`~ipfs_datasets_py.logic.hammers.models.HammerResult` /
  :class:`~ipfs_datasets_py.logic.hammers.receipts.HammerReceipt` is
  byte-for-byte reproducible across test runs (a real kernel invocation's
  own wall-clock timing is, unavoidably, *not* reproducible -- those cases
  are asserted for internal digest self-consistency instead; see
  ``test_end_to_end_hammer.py``).
- One builder function per golden/adversarial case, each wiring together
  real HAMMER-004/007/008/009/010/011/012 code (premise selection,
  translation, provenance normalization, and -- for the "verified" cases --
  a genuine Lean/Coq kernel subprocess) rather than re-implementing any of
  that logic in the test suite itself.
"""


def build_candidate_only_case(
    manifest: CorpusManifest,
) -> Tuple[HammerResult, NormalizedEvidence]:
    """A solver reports a genuine, structurally-parsed TSTP proof listing --
    an untrusted candidate that has not (yet) been reconstructed/kernel
    checked, so the final status must be ``CANDIDATE``, never ``VERIFIED``."""

    corpus_revision = manifest.revision
    goal_statement = "forall x, p(x) -> p(x)"
    request = make_fixed_request(
        request_id="golden-candidate-1",
        theorem_id="hammer_golden_candidate",
        goal_statement=goal_statement,
        corpus_revision=corpus_revision,
        policy=make_fixed_policy(allowed_solvers=["vampire"]),
    )
    premises = select_golden_premises(manifest, goal_statement, top_k=2)

    ctx = TranslationContext(request_id=request.request_id)
    translation = ctx.translate(
        source_construct="goal", term=tautology_term(), target=TranslationTarget.TPTP
    )

    stdout = (
        "% SZS status Theorem for hammer_golden_candidate\n"
        f"fof(f1, axiom, p(a), file('src', '{premises[0].premise_id}')).\n"
        "fof(f2, plain, $false, inference(resolve, [], [f1])).\n"
    )
    attempt = SolverAttemptRecord(
        attempt_id=f"{request.request_id}:{translation.translation_id}:vampire:0",
        request_id=request.request_id,
        translation_id=translation.translation_id,
        solver_name="vampire",
        solver_version="fixture-1.0",
        target=TranslationTarget.TPTP,
        timeout_seconds=request.policy.timeout_seconds,
        verdict=SolverVerdict.PROVED,
        exit_code=0,
        wall_time_seconds=1.0,
        raw_output_digest=compute_content_digest({"stdout": stdout, "stderr": ""}),
        started_at=FIXED_CREATED_AT,
        finished_at=FIXED_COMPLETED_AT,
    )
    attempt.validate()

    normalized = normalize_solver_evidence(
        request_id=request.request_id,
        attempt=attempt,
        raw_stdout=stdout,
        raw_stderr="",
        premise_ids=[p.premise_id for p in premises],
        translation_map=ctx.translation_map,
    )
    assert normalized.recommended_status is HammerResultStatus.CANDIDATE

    candidate = build_proof_candidate_record(
        normalized,
        candidate_id=compute_content_digest({"case": "candidate", "attempt": attempt.attempt_id}),
        request_id=request.request_id,
        solver_attempt_id=attempt.attempt_id,
    )

    result = HammerResult(
        result_id=f"{request.request_id}:result",
        request=request,
        status=HammerResultStatus.CANDIDATE,
        corpus_revision=corpus_revision,
        premises=premises,
        translations=[translation],
        solver_attempts=[attempt],
        proof_candidate=candidate,
        created_at=FIXED_CREATED_AT,
        completed_at=FIXED_COMPLETED_AT,
        notes=["untrusted candidate proof pending kernel reconstruction (HAMMER-010)"],
    )
    return result, normalized



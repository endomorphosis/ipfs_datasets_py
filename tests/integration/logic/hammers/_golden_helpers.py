def build_unavailable_solver_case(manifest: CorpusManifest) -> Tuple[HammerResult, list]:
    """The only allowlisted solver has no resolvable executable in this
    environment -- nothing is ever executed.

    Internally forces solver-executable resolution
    (``ipfs_datasets_py.logic.hammers.policy.shutil.which``) to always
    report "not found" for the duration of this call, so the case is
    deterministic regardless of what happens to be installed on the host
    running the suite (never dependent on the CI/dev host coincidentally
    lacking Vampire).
    """

    from unittest import mock

    from ipfs_datasets_py.logic.hammers import policy as policy_module

    corpus_revision = manifest.revision
    goal_statement = "forall x, p(x) -> p(x)"
    request = make_fixed_request(
        request_id="golden-unavailable-1",
        theorem_id="hammer_golden_unavailable",
        goal_statement=goal_statement,
        corpus_revision=corpus_revision,
        policy=make_fixed_policy(allowed_solvers=["vampire"]),
    )
    premises = select_golden_premises(manifest, goal_statement, top_k=1)

    ctx = TranslationContext(request_id=request.request_id)
    translation = ctx.translate(
        source_construct="goal", term=tautology_term(), target=TranslationTarget.TPTP
    )
    assert translation.status is TranslationStatus.SUPPORTED

    policy = PortfolioPolicy(
        hammer_policy=request.policy, executable_overrides={}
    )
    portfolio = SolverPortfolio(policy)
    with mock.patch.object(policy_module.shutil, "which", return_value=None):
        permitted, denied = portfolio.resolve_attempts(
            [PortfolioAttemptSpec(translation=translation, solver_name="vampire")]
        )
    assert permitted == []
    assert len(denied) == 1

    result = HammerResult(
        result_id=f"{request.request_id}:result",
        request=request,
        status=HammerResultStatus.UNAVAILABLE,
        corpus_revision=corpus_revision,
        premises=premises,
        translations=[translation],
        created_at=FIXED_CREATED_AT,
        completed_at=FIXED_COMPLETED_AT,
        errors=[denied[0]["reason"]],
        notes=["required solver capability not present in this environment; nothing executed"],
    )
    return result, denied


def build_unsupported_translation_case(manifest: CorpusManifest) -> HammerResult:
    """A dependent ``Vec n`` type fails closed at translation time rather
    than being silently dropped."""

    corpus_revision = manifest.revision
    goal_statement = "forall v : Vec n, v = v"
    request = make_fixed_request(
        request_id="golden-unsupported-1",
        theorem_id="hammer_golden_unsupported",
        goal_statement=goal_statement,
        corpus_revision=corpus_revision,
        policy=make_fixed_policy(allowed_solvers=[]),
    )
    premises = select_golden_premises(manifest, goal_statement, top_k=1)

    ctx = TranslationContext(request_id=request.request_id)
    translation = ctx.translate(
        source_construct="goal", term=dependent_type_term(), target=TranslationTarget.TPTP
    )
    assert translation.status is TranslationStatus.UNSUPPORTED
    assert translation.unsupported_reason

    result = HammerResult(
        result_id=f"{request.request_id}:result",
        request=request,
        status=HammerResultStatus.UNSUPPORTED_TRANSLATION,
        corpus_revision=corpus_revision,
        premises=premises,
        translations=[translation],
        created_at=FIXED_CREATED_AT,
        completed_at=FIXED_COMPLETED_AT,
        errors=[translation.unsupported_reason],
        notes=["dependent-type construct cannot be lowered to any supported translation target"],
    )
    return result



def make_fixed_policy(**overrides) -> HammerPolicy:
    defaults = dict(timeout_seconds=20.0, allowed_solvers=[])
    defaults.update(overrides)
    return HammerPolicy(**defaults)


def make_fixed_request(
    *,
    request_id: str,
    theorem_id: str,
    goal_statement: str,
    corpus_revision: str,
    itp: ITPKind = ITPKind.LEAN,
    policy: Optional[HammerPolicy] = None,
) -> HammerRequest:
    """Build a :class:`HammerRequest` stamped with :data:`FIXED_CREATED_AT`
    (rather than the real wall clock) for fully reproducible golden cases."""

    return HammerRequest(
        request_id=request_id,
        itp=itp,
        theorem_id=theorem_id,
        goal_statement=goal_statement,
        corpus_revision=corpus_revision,
        policy=policy if policy is not None else make_fixed_policy(),
        created_at=FIXED_CREATED_AT,
    )


def select_golden_premises(
    manifest: CorpusManifest,
    goal_statement: str,
    *,
    imports: Optional[Iterable[str]] = None,
    top_k: int = 2,
) -> List[PremiseRecord]:
    """Deterministically select premises for ``goal_statement`` from
    ``manifest`` via the real HAMMER-004 baseline selector."""

    goal = GoalFeatures.from_statement(goal_statement, imports=imports)
    result = select_premises(manifest, goal, top_k=top_k)
    return result.selected


# ---------------------------------------------------------------------------
# Shared translation terms
# ---------------------------------------------------------------------------

NAT = SortRef("nat")


def _predicate(name: str) -> Const:
    return Const(name, FunctionTypeRef((NAT,), PROP_SORT))


def tautology_term():
    """``forall x. p(x) => p(x)`` -- trivially valid regardless of format."""

    p = _predicate("p")
    return Forall("x", NAT, Implies(App(p, (Var("x", NAT),)), App(p, (Var("x", NAT),))))


def contradiction_term():
    """``exists x. p(x) and not p(x)`` -- trivially unsatisfiable."""

    p = _predicate("p")
    return Exists("x", NAT, And(App(p, (Var("x", NAT),)), Not(App(p, (Var("x", NAT),)))))


def dependent_type_term():
    """A ``Vec n``-typed equality -- a dependent type HAMMER-007 must fail
    closed on, never silently translate."""

    c = Const("v", DependentTypeRef("Vec n"))
    return Eq(c, c)



def _canonical_state() -> dict:
    """A product table generated by typed additive semantic effects."""

    families = [f"family-{index}" for index in range(8)]
    slots = [f"slot-{index}" for index in range(12)]
    views = [f"view-{index}" for index in range(8)]
    state = {
        TRIPLE_EMBEDDING_HEAD: {},
        LEGAL_IR_VIEW_LOGIT_HEAD: {},
        FAMILY_LOGIT_HEAD: {},
    }
    for family_index, family in enumerate(families):
        for slot_index, slot in enumerate(slots):
            for view_index, view in enumerate(views):
                value = 0.13 * family_index - 0.021 * slot_index + 0.007 * view_index
                state[TRIPLE_EMBEDDING_HEAD][f"{family}||{slot}||{view}"] = [
                    value,
                    -0.5 * value,
                    0.25 + value,
                ]
                state[LEGAL_IR_VIEW_LOGIT_HEAD].setdefault(
                    f"{family}||{slot}", {}
                )[view] = value
                state[FAMILY_LOGIT_HEAD].setdefault(f"{slot}||{view}", {})[
                    family
                ] = -value
    return state


def _semantic_scores() -> dict[str, float]:
    scores = {name: 1.0 for name in SEMANTIC_EQUIVALENCE_METRICS}
    scores[PROOF_OBLIGATION_DELTA] = 0.0
    return scores


def _quality_packet(*, ce: float, cosine: float, obligation: float = 1.0, proof: float = 1.0) -> dict:
    semantic = _semantic_scores()
    semantic["obligation_equivalence"] = obligation
    return {
        "view_family_metrics": {
            "deontic": {
                **semantic,
                "autoencoder_cross_entropy_loss": ce,
                "autoencoder_cosine_similarity": cosine,
            }
        },
        "metrics": {
            "hammer_proof_success_rate": proof,
            "provenance_alignment_success_rate": 1.0,
            "round_trip_reconstruction_success_rate": 1.0,
            "source_copy_reward_hack_penalty": 0.0,
        },
    }


def test_forward_is_typed_additive_plus_configurable_low_rank_interaction() -> None:
    config = FactorizedHeadConfig(rank=1, residual_capacity=0)
    head = FactorizedSemanticInteractionHead(
        name="semantic-test",
        families=("deontic", "temporal"),
        semantic_slots=("actor",),
        legal_ir_views=("deontic.ir",),
        output_labels=("logit", "aux"),
        config=config,
        bias=[0.5, -0.5],
        family_embeddings=[[2.0, 3.0], [4.0, 5.0]],
        semantic_slot_embeddings=[[0.25, 0.75]],
        legal_ir_view_embeddings=[[0.125, -0.125]],
        family_factors=[[2.0], [3.0]],
        semantic_slot_factors=[[5.0]],
        legal_ir_view_factors=[[7.0]],
        output_factors=[[0.1, -0.2]],
    )

    # Typed vocabularies use disjoint stable namespaces.
    assert all(key.kind is TensorKeyKind.FAMILY for key in head.typed_families)
    assert all(key.kind is TensorKeyKind.SEMANTIC_SLOT for key in head.typed_semantic_slots)
    assert all(key.kind is TensorKeyKind.LEGAL_IR_VIEW for key in head.typed_legal_ir_views)
    expected = np.array([0.5, -0.5]) + [2.0, 3.0] + np.array([0.25, 0.75]) + [0.125, -0.125]
    expected += (2.0 * 5.0 * 7.0) * np.array([0.1, -0.2])
    assert head.forward("deontic", "actor", "deontic.ir") == pytest.approx(expected)
    with pytest.raises(KeyError, match="unknown typed interaction"):
        head.forward("unknown", "actor", "deontic.ir")


def test_migration_preserves_canonical_logits_and_reduces_both_state_measures_fourfold() -> None:
    result = migrate_modal_autoencoder_factorized_heads(
        _canonical_state(),
        config=FactorizedStateMigrationConfig(
            rank=0,
            residual_capacity=0,
            tolerance=1.0e-10,
            fitting_steps=0,
        ),
    )

    assert result.accepted is True
    assert result.report.max_absolute_error <= 1.0e-10
    assert result.report.parameter_reduction_ratio >= 4.0
    assert result.report.checkpoint_reduction_ratio >= 4.0
    assert result.report.residual_count == 0
    expected = _canonical_state()[LEGAL_IR_VIEW_LOGIT_HEAD]["family-3||slot-4"]["view-5"]
    assert result.heads.legal_ir_view_logit("family-3", "slot-4", "view-5") == pytest.approx(expected)


def test_checkpoint_round_trip_and_legacy_rollback_materialization_are_deterministic() -> None:
    result = migrate_modal_autoencoder_factorized_heads(
        _canonical_state(),
        config=FactorizedStateMigrationConfig(rank=0, residual_capacity=0, tolerance=1.0e-10),
    )
    encoded = result.heads.to_checkpoint_bytes()
    loaded = load_factorized_head_checkpoint(encoded)
    restored = materialize_legacy_interaction_tables(loaded)

    assert loaded.identity == result.heads.identity
    assert loaded.to_checkpoint_bytes() == encoded
    assert restored[TRIPLE_EMBEDDING_HEAD]["family-2||slot-3||view-4"] == pytest.approx(
        _canonical_state()[TRIPLE_EMBEDDING_HEAD]["family-2||slot-3||view-4"]
    )
    assert restored[LEGAL_IR_VIEW_LOGIT_HEAD]["family-2||slot-3"]["view-4"] == pytest.approx(
        _canonical_state()[LEGAL_IR_VIEW_LOGIT_HEAD]["family-2||slot-3"]["view-4"]
    )
    # The serialized payload is canonical JSON inside a checksummed container.
    assert json.dumps(loaded.to_dict(), sort_keys=True)


def test_sparse_residual_is_error_verified_and_hard_bounded() -> None:
    records = {
        (family, slot, view): float(family[-1]) + float(slot[-1]) + float(view[-1])
        for family in ("f0", "f1")
        for slot in ("s0", "s1")
        for view in ("v0", "v1")
    }
    records[("f1", "s1", "v1")] += 0.75
    head, report = factorize_interaction_records(
        records,
        name="exception-head",
        config=FactorizedHeadConfig(rank=0, residual_capacity=1, tolerance=1.0e-10),
    )

    assert report.logits_preserved is True
    assert head.residual_count == 1
    residual = head.residuals[0]
    assert residual.observed_error > residual.tolerance
    assert residual.verification_id.startswith("migration-verification-")
    assert head.scalar("f1", "s1", "v1") == pytest.approx(records[("f1", "s1", "v1")])

    with pytest.raises(UnverifiedResidualError, match="observed error"):
        VerifiedResidual("f0", "s0", "v0", (0.1,), "receipt", 0.0, 0.0)
    with pytest.raises(ResidualCapacityError, match="capacity"):
        factorize_interaction_records(
            records,
            name="no-exceptions",
            config=FactorizedHeadConfig(rank=0, residual_capacity=0, tolerance=1.0e-10),
        )


def test_rank_one_migration_recovers_a_true_typed_three_way_interaction() -> None:
    families = ("f0", "f1", "f2", "f3")
    slots = ("s0", "s1", "s2", "s3", "s4")
    views = ("v0", "v1", "v2", "v3", "v4", "v5")
    records = {
        (family, slot, view): 0.01
        * (family_index - 1.5)
        * (slot_index - 2.0)
        * (view_index - 2.5)
        for family_index, family in enumerate(families)
        for slot_index, slot in enumerate(slots)
        for view_index, view in enumerate(views)
    }

    head, report = factorize_interaction_records(
        records,
        name="rank-one-head",
        config=FactorizedHeadConfig(
            rank=1,
            residual_capacity=0,
            tolerance=1.0e-10,
            fitting_steps=100,
        ),
    )

    assert head.rank == 1
    assert head.residual_count == 0
    assert report.max_absolute_error <= 1.0e-10
    assert head.scalar("f3", "s4", "v5") == pytest.approx(
        records[("f3", "s4", "v5")]
    )


def test_rank_and_residual_ablations_are_explicit_and_non_mutating() -> None:
    base = FactorizedHeadConfig(rank=7, residual_capacity=23)
    arms = factorized_head_ablation_configs(base, ranks=(0, 3))

    assert set(arms) == {
        "rank_0_residual_off",
        "rank_0_residual_on",
        "rank_3_residual_off",
        "rank_3_residual_on",
    }
    assert arms["rank_0_residual_on"].effective_rank == 0
    assert arms["rank_3_residual_on"].effective_rank == 3
    assert arms["rank_3_residual_off"].effective_residual_capacity == 0
    assert base.rank == 7 and base.residual_capacity == 23


def test_quality_gate_rejects_ce_cosine_gain_that_degrades_semantics_or_proof() -> None:
    baseline = _quality_packet(ce=0.8, cosine=0.7)
    candidate = _quality_packet(ce=0.3, cosine=0.9, obligation=0.5, proof=0.8)
    config = FactorizedHeadQualityGateConfig(semantic_families=("deontic",))

    report = evaluate_factorized_head_quality(baseline, candidate, config=config)

    assert report.ce_or_cosine_improved is True
    assert report.disagreement is True
    assert report.accepted is False
    assert "proof_validity" in report.protected_regressions
    assert "family_semantic_equivalence_failed" in report.block_reasons
    with pytest.raises(FactorizedQualityGateError, match="candidate rejected"):
        require_factorized_head_quality(baseline, candidate, config=config)


def test_quality_gate_accepts_objective_gain_only_when_every_guardrail_is_preserved() -> None:
    baseline = _quality_packet(ce=0.8, cosine=0.7)
    candidate = _quality_packet(ce=0.4, cosine=0.9)
    report = evaluate_factorized_head_quality(
        baseline,
        candidate,
        config=FactorizedHeadQualityGateConfig(semantic_families=("deontic",)),
    )

    assert report.ce_or_cosine_improved is True
    assert report.protected_regressions == {}
    assert report.missing_categories == ()
    assert report.accepted is True
